#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	c;

	c = 0;
	sign = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		c = c * 10 + *str - '0';
		str++;
	}
	return (sign * c);
}
